namespace Class;

public class Colculater
{
    public double Summa (double A,double B)
    {
        return A+B;
    }
    public double Difference (double A,double B)
    {
        return A-B;
    }
    public double Multiply (double A,double B)
    {
        return A*B;
    }
    public double Division (double A,double B)
    {
        return A/B;
    }
}
